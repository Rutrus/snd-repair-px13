#!/usr/bin/env bash
# Regenerate research/phase-7/proposed/0006a-validate-manager-mask.patch (incremental on 0006b).
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
# shellcheck source=lib/common.sh
source "$SCRIPT_DIR/lib/common.sh"

SRC="$KERNEL_SRC"
REPO="$REPO_ROOT"
OUT="$REPO/research/phase-7/proposed/0006a-validate-manager-mask.patch"
TMP="$(mktemp -d)"
trap 'rm -rf "$TMP"' EXIT

cd "$SRC"
if ! rg -q 'fn=device_state_D0' drivers/soundwire/amd_manager.c 2>/dev/null; then
	echo "Phase 6 0007 base missing — run build-phase6-amd-trace.sh first" >&2
	exit 1
fi

cp drivers/soundwire/amd_manager.c "$TMP/base.c"

PATCH_0006B="$REPO/research/phase-7/proposed/0006b-stat-decode.patch"
[[ -f "$PATCH_0006B" ]] || { echo "Missing $PATCH_0006B — run regenerate-phase7-0006b.sh" >&2; exit 1; }
patch -p1 --forward -d "$SRC" <"$PATCH_0006B" >/dev/null
cp drivers/soundwire/amd_manager.c "$TMP/0006b.c"

python3 - "$SRC/drivers/soundwire/amd_manager.c" <<'PY'
from pathlib import Path
import sys
p = Path(sys.argv[1])
t = p.read_text()

manual_fn = '''/* Phase 7 experiment 0006a — falsification: schedule irq_thread if STAT&manager_mask. */
static void snd_repair_phase7_try_manual_irq(struct device *dev,
					     struct amd_sdw_manager *amd_manager,
					     struct sdw_bus *bus, const char *when)
{
	void __iomem *acp = amd_manager->acp_mmio;
	unsigned int inst = amd_manager->instance;
	u32 manager_mask = sdw_manager_reg_mask_array[inst];
	u32 stat_i, stat_and_mask;

	if (!acp)
		return;

	stat_i = readl(acp + ACP_EXTERNAL_INTR_STAT(inst));
	stat_and_mask = stat_i & manager_mask;

	if (stat_and_mask) {
		dev_info(dev,
			 "PHASE7 ctx=amd fn=manual_irq_schedule reason=STAT&mask when=%s link=%d resume=%d manager=%u stat=0x%x mask=0x%x hit=0x%x\\n",
			 when, bus->link_id, amd_phase6_resume_id(amd_manager), inst,
			 stat_i, manager_mask, stat_and_mask);
		schedule_work(&amd_manager->amd_sdw_irq_thread);
	} else {
		dev_info(dev,
			 "PHASE7 ctx=amd fn=manual_irq_schedule skipped when=%s link=%d resume=%d manager=%u stat=0x%x mask=0x%x\\n",
			 when, bus->link_id, amd_phase6_resume_id(amd_manager), inst,
			 stat_i, manager_mask);
	}
}

'''
if 'snd_repair_phase7_try_manual_irq' not in t:
    t = t.replace(
        "}\n\nstatic int __maybe_unused amd_resume_runtime(struct device *dev)",
        "}\n\n" + manual_fn + "static int __maybe_unused amd_resume_runtime(struct device *dev)", 1)

old_block = (
    "\t\tsnd_repair_phase7_intr_decode(dev, amd_manager, bus, \"post_D0\");\n"
    "\t\tif (snd_repair_phase7_delay_ms) {\n"
    "\t\t\tdev_info(dev,\n"
    "\t\t\t     \"PHASE7 ctx=amd fn=delay_before_decode link=%d resume=%d delay_ms=%u\\n\",\n"
    "\t\t\t     bus->link_id, amd_phase6_resume_id(amd_manager),\n"
    "\t\t\t     snd_repair_phase7_delay_ms);\n"
    "\t\t\tmsleep(snd_repair_phase7_delay_ms);\n"
    "\t\t\tsnd_repair_phase7_intr_decode(dev, amd_manager, bus, \"post_delay\");\n"
    "\t\t}\n"
    "\t\tif (ret)")

new_block = (
    "\t\tsnd_repair_phase7_intr_decode(dev, amd_manager, bus, \"post_D0\");\n"
    "\t\tif (snd_repair_phase7_delay_ms) {\n"
    "\t\t\tdev_info(dev,\n"
    "\t\t\t     \"PHASE7 ctx=amd fn=delay_before_decode link=%d resume=%d delay_ms=%u\\n\",\n"
    "\t\t\t     bus->link_id, amd_phase6_resume_id(amd_manager),\n"
    "\t\t\t     snd_repair_phase7_delay_ms);\n"
    "\t\t\tmsleep(snd_repair_phase7_delay_ms);\n"
    "\t\t\tsnd_repair_phase7_intr_decode(dev, amd_manager, bus, \"post_delay\");\n"
    "\t\t\tsnd_repair_phase7_try_manual_irq(dev, amd_manager, bus, \"post_delay\");\n"
    "\t\t} else {\n"
    "\t\t\tsnd_repair_phase7_try_manual_irq(dev, amd_manager, bus, \"post_D0\");\n"
    "\t\t}\n"
    "\t\tif (ret)")

if 'snd_repair_phase7_try_manual_irq(dev, amd_manager, bus, "post_delay")' not in t:
    if old_block not in t:
        raise SystemExit("0006a resume anchor missing — run regenerate-phase7-0006b.sh first")
    t = t.replace(old_block, new_block, 1)

p.write_text(t)
PY

diff -u "$TMP/0006b.c" "$SRC/drivers/soundwire/amd_manager.c" >"$TMP/0006a.diff" || true
{
	cat <<'EOF'
From: snd-repair phase7 <snd-repair@local>
Date: Fri, 10 Jul 2026 23:00:00 +0200
Subject: [PATCH phase7 0006a] soundwire: amd: manual irq_thread if STAT&mask

Applies on 0006b-stat-decode.patch (Phase 6 + 0006b required). NOT a fix.

Regenerated by scripts/regenerate-phase7-0006a.sh (diff -u).

Signed-off-by: snd-repair phase7 <snd-repair@local>
---
EOF
	sed -e '1s|--- .*|--- a/drivers/soundwire/amd_manager.c|' \
	    -e '2s|+++ .*|+++ b/drivers/soundwire/amd_manager.c|' "$TMP/0006a.diff"
} >"$OUT"

cp "$TMP/0006b.c" "$SRC/drivers/soundwire/amd_manager.c"
patch -p1 --dry-run --forward <"$OUT" >/dev/null
echo "Wrote $OUT (dry-run OK on 0006b base)"
