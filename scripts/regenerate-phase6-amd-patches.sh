#!/usr/bin/env bash
# Regenerate Phase 6 AMD trace patches (0004–0007) from a clean 0003 base via diff.
# Fixes malformed line-count / indentation issues in hand-written unified diffs.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
# shellcheck source=lib/common.sh
source "$SCRIPT_DIR/lib/common.sh"

SRC="$KERNEL_SRC"
REPO="$REPO_ROOT"
TMP="$(mktemp -d)"
trap 'rm -rf "$TMP"' EXIT

reset_and_0003() {
	"$SCRIPT_DIR/reset-phase6-amd-manager.sh" >/dev/null
	cd "$SRC"
	patch -p1 --forward <"$REPO/research/phase-6/proposed/0003-phase6-amd-sdw-trace.patch" >/dev/null
	cp drivers/soundwire/amd_manager.c "$TMP/base.c"
	cp sound/soc/amd/ps/pci-ps.c "$TMP/pci-base.c"
}

write_patch() {
	local id="$1" subject="$2" amd_diff="$3" ps_diff="${4:-}"
	local out
	case "$id" in
	0004*) out="$REPO/research/phase-6/proposed/0004-phase6-amd-minimal-irq-trace.patch" ;;
	0005*) out="$REPO/research/phase-6/proposed/0005-phase6-s1-s2-bisect.patch" ;;
	0006*) out="$REPO/research/phase-6/proposed/0006-phase6-acp-block-state.patch" ;;
	0007*) out="$REPO/research/phase-6/proposed/0007-phase6-resume-kick-trace.patch" ;;
	esac
	{
		cat <<EOF
From: snd-repair phase6 <snd-repair@local>
Date: Fri, 10 Jul 2026 21:15:00 +0200
Subject: [PATCH phase6] $subject

Regenerated by scripts/regenerate-phase6-amd-patches.sh (diff -u).

Signed-off-by: snd-repair phase6 <snd-repair@local>
---
EOF
		sed -e '1s|--- .*|--- a/drivers/soundwire/amd_manager.c|' \
		    -e '2s|+++ .*|+++ b/drivers/soundwire/amd_manager.c|' "$amd_diff"
		if [[ -n "$ps_diff" && -s "$ps_diff" ]]; then
			sed -e '1s|--- .*|--- a/sound/soc/amd/ps/pci-ps.c|' \
			    -e '2s|+++ .*|+++ b/sound/soc/amd/ps/pci-ps.c|' "$ps_diff"
		fi
	} >"$out"
	echo "Wrote $out"
}

