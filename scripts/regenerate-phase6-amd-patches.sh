diff_step() {
	local prev="$1" label="$2" subject="$3" ps="${4:-0}"
	local prev_ps="${5:-$TMP/pci-base.c}"
	diff -u "$prev" "$SRC/drivers/soundwire/amd_manager.c" >"$TMP/${label}-amd.diff" || true
	if [[ "$ps" == "1" ]]; then
		diff -u "$prev_ps" "$SRC/sound/soc/amd/ps/pci-ps.c" >"$TMP/${label}-ps.diff" || true
		write_patch "$label" "$subject" "$TMP/${label}-amd.diff" "$TMP/${label}-ps.diff"
		cp "$SRC/sound/soc/amd/ps/pci-ps.c" "$TMP/${label}-pci.c"
	else
		write_patch "$label" "$subject" "$TMP/${label}-amd.diff"
	fi
	cp "$SRC/drivers/soundwire/amd_manager.c" "$TMP/${label}.c"
}

