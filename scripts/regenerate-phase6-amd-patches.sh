apply_0006
diff_step "$TMP/0005.c" "0006" "ACP block state (0006)"

apply_0007
diff_step "$TMP/0006.c" "0007" "resume kick trace (0007)"

echo "==> Verifying clean apply chain"
reset_and_0003
for p in 0004 0005 0006 0007; do
	patch -p1 --forward -d "$SRC" <"$REPO/research/phase-6/proposed/${p}-phase6"*.patch
done
rg -q 'fn=device_state_D0' "$SRC/drivers/soundwire/amd_manager.c"
rg -q 'fn=irq_handler_enter' "$SRC/sound/soc/amd/ps/pci-ps.c"
rg -q 'fn=intr_stat_post_enable' "$SRC/drivers/soundwire/amd_manager.c"
echo "OK: regenerated patches apply cleanly on 0003 base"
